<p>Diese Anleitung dient der Konstruktion eines Grundschnitts für einen Fullcup-BH mit dreigeteiltem Cup. Dieser Schnitt ist auch für große Brüste geeignet. Der BH sollte mit Bügel genäht werden; die Cups können aus festem, nicht dehnbaren Stoff mit oder ohne Schaumstoffcup als Unterbau ausgeführt werden. Das eigentliche Nähen wird hier nicht erklärt, da es dazu bereits zahlreiche Anleitungen im Internet, sowie in Büchern gibt.</p>
